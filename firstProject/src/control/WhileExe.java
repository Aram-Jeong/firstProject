package control;

public class WhileExe {

	public static void main(String[] args) {
		System.out.println("===1번===");
		int i = 1;
		while (i <= 10) {
			System.out.printf("번호 %d\n", i);
			i++;
		}
		System.out.println("===2번===");
		int n = 1;
		while (n <= 10) {
			n += 1;
			System.out.printf("번호 %d\n", n);
			n++;

		}
		System.out.println("===3번===");
	}

}
